const mongoose = require('mongoose')



const ContactSchema  = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    phone: {
        type: String,
        required: true
    },
    country: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    },
    is_seen: {
        type: Number,
        required: true
    },
    created_at: {
        type: Date,
        required: true
    },
    updated_at: {
        type: Date,
        required: true
    },
})



const ContactModel = mongoose.model("contacts", ContactSchema)

module.exports =  ContactModel